package day01;

public class Primitives {

    public static void main(String[] args) {

        //DataType var = data

        int  num1 = 100;
        double num2 = 100; //100.0
        char ch1 = 16000;
        boolean r1 =  45 > 30;

        System.out.println(ch1);

        System.out.println("------------------------------");

        byte  b = 100;
        short s = (short)b;  //implicit casting

        int i = 300;
        long l = i;   //implicit casting

        System.out.println("------------------------------");

        long n1 = 500;
        int n2 = (int) n1; // explicit casting




    }

}
